/** 
    변수 
        - 변수란 특정 값을 저장하는 공간
        - 데이터를 저장하고 참조(사용)하는 데이터의 이름
        - 키워드(var, let, const) 변수이름 = 값

    변수 규칙
        - 이름으로는 문자 / 숫자 / $ / _
        - 첫글자는 숫자가 될 수 없다.
        - 예약어도 사용할 수 없다.
        - 상수는 대문자로 선언함

 * */ 

var number = 5; // 변수의 선언과 할당
var number1; // 변수 선언
number1 = 5; // 변수에 값을 할당한다.

let string = "가나다라마바사";
const string2 = "가가가";

// var let const
// var : 재선언 가능, 재할당 가능
// let : 재선언 불가능, 재할당은 가능
// const : 재선언 불가능, 재할당 불가능

